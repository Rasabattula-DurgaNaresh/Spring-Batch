package de.viadee.spring.batch.infrastructure;

import org.apache.log4j.Logger;

/**
 * This is some kind of a Log4J Proxy. Its purpose is to give the developer the ability to redirect the tool-based Log4J
 * Output in order to separate it from the Batch-Processes Log. See "log4j.additivity" in the Log4J documentation for
 * further details.
 * 
 */

public final class LoggingWrapper {

    public static Logger getLogger(final Class<?> clazz) {
        return Logger.getLogger("SpringBatchMonitoring." + clazz.getName());
    }

    private LoggingWrapper() {

    }

}
